# This beginner project is about string concatenation. Input is requested to insert
# an adjective, a football team and a football player to return the full madlib.
adj = input("Adjective: ")
football_team = input("Football team: ")
football_player = input("Football player: ")

madlib = f"I absolutely love football, it is so {adj}! My team is {football_team} \
and my favourite player is {football_player}."

print(madlib)